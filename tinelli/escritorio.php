                        <h3>Nossa história</h3>
                        <p>O escritório nasceu com a sócia Carolina Tinelli Ferrarini em 2013, que se formou em 2008. Especialista em Direito Empresarial com ênfase em Direito Imobiliário e em Direito Notarial e Registral Imobiliário. Fortemente atuante com Registros Públicos, muito especialmente junto a cartórios de registro de imóveis. Atua em áreas como Direito Tributário e Cível em geral. Atualmente conta com um novo sócio, Vitor Tunussi Verdugo.</p>
						<p>O escritório Tinelli Ferrarini Sociedade opera em três pilares essenciais: comprometimento pleno com as atividades de nossos clientes, proporcionar soluções jurídicas e oferecer o que há de melhor em técnica e prática jurídica.</p>
						<p>Extremamente atuante em Americana e Região, Tinelli Ferrarini é um escritório que trabalha com pioneirismo, liderança e responsabilidade na prestação de serviços personalizados e altamente especializados nas áreas que compõem o Direito Empresarial, Civil, Imobiliário, Societário, Contratual, Tributário e Cível Contencioso.</p>
						<p>O escritório trabalha e oferece assessoria e consultoria jurídica, advocacia preventiva e contenciosa, elaboração e análise de contratos, todo tipo de regularização para documentos imobiliários, imóveis urbanos e rurais, e documentação para empreendimentos imobiliários, como incorporações e loteamentos, inventários, posse, usucapião, doação, locação, compra e venda, permutas, desmembramentos, dação em pagamento, constituição de hipotecas e outras garantias reais.</p>
						<p>Constantemente envolvido em transações sofisticadas e complexas, representando pessoas físicas e empresas dos mais diversos segmentos, em operação ou em busca de novos negócios. Os profissionais da Tinelli Ferrarini formam uma equipe altamente talentosa, experiente e comprometida em oferecer o melhor aos seus clientes.</p>
